class User {
  final String email;
  final String password;
  final String fName;
  final String lName;
  final String username;
  final String avatar;
  final String coverImage;
  final int followers;
  final int following;
  final String univ;
  final String bio;

  User({
    required this.email,
    required this.password,
    required this.fName,
    required this.lName,
    required this.username,
    required this.avatar,
    required this.coverImage,
    required this.followers,
    required this.following,
    required this.univ,
    required this.bio,
  });
}
